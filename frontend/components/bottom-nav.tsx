export function BottomNav() {
  const pathname = usePathname();
  const [pendingHref, setPendingHref] = useState<string | null>(null);

  useEffect(() => {
    setPendingHref(null);
  }, [pathname]);

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-20 rounded-t-2xl border-t border-white/15 bg-[#1a1d25]/95 backdrop-blur md:hidden">
      <div className="flex items-stretch" style={{ paddingBottom: "env(safe-area-inset-bottom)" }}>
        {ITEMS.map((item) => {
          const active = pathname === item.href;
          const pending = pendingHref === item.href && !active;
          return (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => { if (!active) setPendingHref(item.href); }}
              aria-label={item.label}
              className={`flex flex-1 items-center justify-center py-4 transition ${
                active
                  ? "text-white"
                  : pending
                  ? "animate-pulse text-white/60"
                  : "text-white/35 hover:text-white/70"
              }`}
            >
              {item.icon}
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
